package com.gqgx.common.entity.vo;

/**
 * cookie中保存的数据对象
 * @author Administrator
 *
 */
public class CookieVo {
	
	/**
	 * 保存的数据
	 */
	private String name;
	
	/**
	 * 保存的id
	 */
	private Long id;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	
}
